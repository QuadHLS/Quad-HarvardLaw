-- CHECK TABLE SCHEMAS
-- This script checks the actual structure of tables to understand their columns
-- before creating RLS policies

-- ==============================================
-- Step 1: Check table structures
-- ==============================================

-- Check calendar_events table structure
SELECT 
    column_name,
    data_type,
    is_nullable,
    column_default
FROM information_schema.columns 
WHERE table_schema = 'public' 
AND table_name = 'calendar_events'
ORDER BY ordinal_position;

